package main

import (
	"os"

	"github.com/labstack/echo/v4"
	"github.com/labstack/echo/v4/middleware"
)

func main() {
	// Echo instance
	e := echo.New()

	// Middleware
	e.Use(middleware.Logger())
	e.Use(middleware.Recover())

	// Routes
	HelloRoute(e.Group("/hello"))

	// Start server
	e.Logger.Fatal(e.Start(":" + os.Getenv("PORT")))
}
